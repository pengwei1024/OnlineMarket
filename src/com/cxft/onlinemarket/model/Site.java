package com.cxft.onlinemarket.model;

public class Site {
	
	private String site_name;
	private String site_key;
	private String site_describe;
	
	public Site() {
		super();
	}

	public String getSite_name() {
		return site_name;
	}

	public void setSite_name(String site_name) {
		this.site_name = site_name;
	}

	public String getSite_key() {
		return site_key;
	}

	public void setSite_key(String site_key) {
		this.site_key = site_key;
	}

	public String getSite_describe() {
		return site_describe;
	}

	public void setSite_describe(String site_describe) {
		this.site_describe = site_describe;
	}
	
	

}
